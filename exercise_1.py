gradecalc = int(input("Enter the grade received between 0-100: "))

if gradecalc >= 90:
    print("Your grade is an A")
elif gradecalc >= 80:
    print("Your grade is a B")
elif gradecalc >= 70:
    print("Your grade is a C")
elif gradecalc >= 60:
    print("Your grade is a D")
elif gradecalc <= 59:
    print("Your grade is a F")